import { readFile, writeFile } from "node:fs/promises";
import vm from "node:vm";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const indexPath = path.join(rootDir, "src", "index.ts");
const outPath = path.join(rootDir, "examples.md");

const source = await readFile(indexPath, "utf8");
const startToken = "const TOOL_DEFINITIONS =";
const endToken = "server.setRequestHandler(ListToolsRequestSchema";
const startIndex = source.indexOf(startToken);
const endIndex = source.indexOf(endToken);

if (startIndex === -1 || endIndex === -1 || endIndex <= startIndex) {
  throw new Error("Could not find TOOL_DEFINITIONS boundaries in src/index.ts");
}

const block = source.slice(startIndex, endIndex);
const arrayStart = block.indexOf("[");
const arrayEnd = block.lastIndexOf("];");

if (arrayStart === -1 || arrayEnd === -1 || arrayEnd <= arrayStart) {
  throw new Error("Could not extract TOOL_DEFINITIONS array from src/index.ts");
}

const toolDefinitionsSource = block.slice(arrayStart, arrayEnd + 1);
const toolDefinitions = vm.runInNewContext(toolDefinitionsSource);
if (!Array.isArray(toolDefinitions)) {
  throw new Error("Parsed TOOL_DEFINITIONS is not an array");
}

const lines = [];
lines.push("# MCP Tool Examples");
lines.push("");
lines.push("Autogenerated from `TOOL_DEFINITIONS` in `src/index.ts`.");
lines.push("Includes smart examples for common workflows and schema-based fallbacks.");
lines.push("Run `npm run generate:examples` after updating tools.");
lines.push("");

let currentGroup = "";
for (const tool of toolDefinitions) {
  const group = groupForTool(tool.name);
  if (group !== currentGroup) {
    currentGroup = group;
    lines.push(`## ${group}`);
    lines.push("");
  }

  lines.push(`### \`${tool.name}\``);
  lines.push("```json");
  lines.push(JSON.stringify(exampleForTool(tool), null, 2));
  lines.push("```");
  lines.push("");
}

await writeFile(outPath, `${lines.join("\n").trimEnd()}\n`, "utf8");
process.stdout.write(`Generated ${path.relative(rootDir, outPath)}\n`);

