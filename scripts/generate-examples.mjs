function exampleForTool(tool) {
  const smart = getSmartExamples()[tool.name];
  if (smart) {
    return smart;
  }
  return exampleFromSchema(tool.inputSchema);
}

function groupForTool(name) {
  if (name.includes("project_variable")) return "Project Variables";
  if (name.includes("merge_request_discussion")) return "MR Discussions";
  if (name.includes("merge_request") || name.includes("issue")) return "Issues and Merge Requests";
  if (name.includes("pipeline")) return "Pipelines";
  if (name.includes("job") || name.includes("artifact")) return "Jobs and Artifacts";
  if (name.includes("deployment")) return "Deployments";
  if (name.includes("environment")) return "Environments";
  if (name.includes("release_link")) return "Release Links";
  if (name.includes("release") || name.includes("changelog")) return "Releases and Changelog";
  if (name.includes("protected_branch") || name.includes("branch")) return "Branches";
  if (name.includes("protected_tag") || name.includes("tag")) return "Tags";
  if (name.includes("file") || name.includes("commit")) return "Repository Files and Commits";
  if (name.includes("project")) return "Projects";
  return "Other";
}

function exampleFromSchema(schema) {
  if (!schema || schema.type !== "object") {
    return {};
  }

  const result = {};
  const properties = schema.properties ?? {};
  const required = Array.isArray(schema.required) ? schema.required : [];

  for (const key of required) {
    result[key] = valueForProperty(properties[key], key);
  }

  return result;
}

function valueForProperty(property, keyName) {
  if (!property) return null;

  if (Object.prototype.hasOwnProperty.call(property, "default")) {
    return property.default;
  }

  if (Array.isArray(property.enum) && property.enum.length > 0) {
    return property.enum[0];
  }

  if (Array.isArray(property.oneOf) && property.oneOf.length > 0) {
    return valueForProperty(property.oneOf[0], keyName);
  }

  if (property.type === "string") {
    return stringExample(keyName);
  }

  if (property.type === "number" || property.type === "integer") {
    return numberExample(keyName);
  }

  if (property.type === "boolean") {
    return false;
  }

  if (property.type === "array") {
    return [valueForProperty(property.items ?? { type: "string" }, keyName)];
  }

  if (property.type === "object") {
    const nested = {};
    const nestedProps = property.properties ?? {};
    const nestedRequired = Array.isArray(property.required) ? property.required : [];
    for (const nestedKey of nestedRequired) {
      nested[nestedKey] = valueForProperty(nestedProps[nestedKey], nestedKey);
    }
    if (nestedRequired.length === 0) {
      const firstKey = Object.keys(nestedProps)[0];
      if (firstKey) {
        nested[firstKey] = valueForProperty(nestedProps[firstKey], firstKey);
      }
    }
    return nested;
  }

  return null;
}

function stringExample(key) {
  const lower = key.toLowerCase();
  if (lower.includes("projectidorpath")) return "group/project";
  if (lower.includes("url")) return "https://example.com";
  if (lower.includes("branch")) return "main";
  if (lower.includes("ref")) return "main";
  if (lower.includes("sha")) return "abc123def456";
  if (lower.includes("file")) return "README.md";
  if (lower.includes("tag")) return "v1.0.0";
  if (lower.includes("discussionid")) return "discussion-id";
  if (lower.includes("commitmessage")) return "chore: update file";
  if (lower.includes("message")) return "example message";
  if (lower.includes("title")) return "Example title";
  if (lower.includes("body") || lower.includes("description") || lower.includes("content")) {
    return "Example text";
  }
  if (lower.includes("key")) return "EXAMPLE_KEY";
  if (lower.includes("value")) return "example-value";
  return "example";
}

function numberExample(key) {
  const lower = key.toLowerCase();
  if (lower.includes("perpage")) return 20;
  if (lower.includes("id") || lower.includes("iid")) return 1;
  return 1;
}
